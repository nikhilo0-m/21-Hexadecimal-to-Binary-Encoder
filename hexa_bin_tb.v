`timescale 1ns / 1ps

module hexa_bin_tb(

    );
    
    reg [15:0] in;
    wire [3:0] out;
    
    hexa_bin dut (in, out);
    initial begin
    
    $display("Time\tInput\t\t\tOutput");
    $monitor("%0t\t%b\t%b", $time, in, out);
    
    in = 16'b0000000000000001; #10; // 0 -> 0000
    in = 16'b0000000000000010; #10; // 1 -> 0001
    in = 16'b0000000000000100; #10; // 2 -> 0010
    in = 16'b0000000000001000; #10; // 3 -> 0011
    in = 16'b0000000000010000; #10; // 4 -> 0100
    in = 16'b0000000000100000; #10; // 5 -> 0101
    in = 16'b0000000001000000; #10; // 6 -> 0110
    in = 16'b0000000010000000; #10; // 7 -> 0111
    in = 16'b0000000100000000; #10; // 8 -> 1000
    in = 16'b0000001000000000; #10; // 9 -> 1001
    in = 16'b0000010000000000; #10; // A -> 1010
    in = 16'b0000100000000000; #10; // B -> 1011
    in = 16'b0001000000000000; #10; // C -> 1100
    in = 16'b0010000000000000; #10; // D -> 1101
    in = 16'b0100000000000000; #10; // E -> 1110
    in = 16'b1000000000000000; #10; // F -> 1111
    
    $finish;
  end
endmodule
